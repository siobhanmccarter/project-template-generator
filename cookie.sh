# Create a project directory structure

# NOTE: make sure you are running this script in the local file or git repo
# in which you want to be creating your project!

touch README.md # create your README file
echo "Here is where you could write out your README" > README.md

touch LICENSE.md # create your LICENSE file
echo "Here is where you would add your license" > LICENSE.md

mkdir "data" # this is where you store your raw data
mkdir "doc" # this is where you store your documents
mkdir "results" # this is where you store your results
mkdir "src" # this is where you store your project source code
mkdir "misc" # this is for your miscellaneous items e.g. random files
              # from a coworker
